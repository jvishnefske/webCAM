#[cfg(test)]
mod tests {
    use super::*;
    extern crate std;

    fn example_manifest() -> DeploymentManifest {
        DeploymentManifest {
            topology: SystemTopology {
                nodes: alloc::vec![
                    BoardNode {
                        id: s("flight_ctrl"),
                        mcu_family: s("Stm32f4"),
                        board: Some(s("FC_V2")),
                        rust_target: Some(s("thumbv7em-none-eabihf")),
                    },
                    BoardNode {
                        id: s("sensor_hub"),
                        mcu_family: s("Esp32c3"),
                        board: None,
                        rust_target: Some(s("riscv32imc-unknown-none-elf")),
                    },
                ],
                links: alloc::vec![PhysicalLink {
                    id: s("can_bus_1"),
                    from_node: s("flight_ctrl"),
                    from_peripheral: s("FDCAN1"),
                    to_node: s("sensor_hub"),
                    to_peripheral: s("TWAI0"),
                    transport: Transport::Can,
                },],
            },
            tasks: alloc::vec![
                TaskBinding {
                    name: s("pid_loop"),
                    node: s("flight_ctrl"),
                    blocks: alloc::vec![1, 2, 3],
                    trigger: TaskTrigger::Periodic { hz: 1000.0 },
                    priority: 1,
                    stack_size: Some(4096),
                },
                TaskBinding {
                    name: s("sensor_read"),
                    node: s("sensor_hub"),
                    blocks: alloc::vec![4, 5],
                    trigger: TaskTrigger::Periodic { hz: 100.0 },
                    priority: 2,
                    stack_size: None,
                },
            ],
            channels: alloc::vec![
                ChannelBinding {
                    name: s("Motor_Torque_Cmd"),
                    from_task: s("pid_loop"),
                    from_port: (3, 0),
                    to_task: s("pid_loop"),
                    to_port: (2, 0),
                    transport: ChannelTransport::IntraNode { depth: 4 },
                },
                ChannelBinding {
                    name: s("Sensor_Data"),
                    from_task: s("sensor_read"),
                    from_port: (5, 0),
                    to_task: s("pid_loop"),
                    to_port: (1, 0),
                    transport: ChannelTransport::InterNode {
                        link: s("can_bus_1"),
                        topic: s("sensor_data"),
                        encoding: Encoding::CanFrame,
                    },
                },
            ],
            peripheral_bindings: alloc::vec![PeripheralBinding {
                block_id: 2,
                port_name: s("PWM_Out"),
                node: s("flight_ctrl"),
                peripheral: s("TIM1"),
                pins: alloc::vec![PinBinding {
                    signal: s("CH1"),
                    pin: s("PA8"),
                    af: Some(1),
                }],
                dma: None,
                config: PeripheralConfig::Pwm {
                    frequency_hz: 20_000,
                    dead_time_ns: None,
                },
            },],
        }
    }

    fn s(v: &str) -> String {
        String::from(v)
    }

    #[test]
    fn validate_valid_manifest() {
        let m = example_manifest();
        assert!(validate_manifest(&m).is_ok());
    }

    #[test]
    fn validate_unknown_node_in_task() {
        let mut m = example_manifest();
        m.tasks[0].node = s("unknown_node");
        let errs = validate_manifest(&m).unwrap_err();
        assert!(errs.iter().any(|e| e.contains("unknown node")));
    }

    #[test]
    fn validate_unknown_task_in_channel() {
        let mut m = example_manifest();
        m.channels[0].from_task = s("nonexistent");
        let errs = validate_manifest(&m).unwrap_err();
        assert!(errs.iter().any(|e| e.contains("unknown source task")));
    }

    #[test]
    fn validate_unknown_link_in_channel() {
        let mut m = example_manifest();
        m.channels[1] = ChannelBinding {
            name: s("bad"),
            from_task: s("sensor_read"),
            from_port: (5, 0),
            to_task: s("pid_loop"),
            to_port: (1, 0),
            transport: ChannelTransport::InterNode {
                link: s("nonexistent_link"),
                topic: s("x"),
                encoding: Encoding::RawF64,
            },
        };
        let errs = validate_manifest(&m).unwrap_err();
        assert!(errs.iter().any(|e| e.contains("unknown link")));
    }

    #[test]
    fn validate_duplicate_block_assignment() {
        let mut m = example_manifest();
        // Block 3 in both tasks
        m.tasks[1].blocks.push(3);
        let errs = validate_manifest(&m).unwrap_err();
        assert!(errs.iter().any(|e| e.contains("multiple tasks")));
    }

    #[test]
    fn serde_roundtrip_manifest() {
        let m = example_manifest();
        let json = serde_json::to_string_pretty(&m).unwrap();
        let parsed: DeploymentManifest = serde_json::from_str(&json).unwrap();
        assert_eq!(parsed.topology.nodes.len(), 2);
        assert_eq!(parsed.tasks.len(), 2);
        assert_eq!(parsed.channels.len(), 2);
        assert_eq!(parsed.peripheral_bindings.len(), 1);
    }

    #[test]
    fn serde_task_trigger_variants() {
        let triggers = alloc::vec![
            TaskTrigger::Periodic { hz: 1000.0 },
            TaskTrigger::Interrupt { irq: s("USART1") },
            TaskTrigger::Event {
                channel: s("Motor_Cmd")
            },
            TaskTrigger::Init,
        ];
        for t in triggers {
            let json = serde_json::to_string(&t).unwrap();
            let _: TaskTrigger = serde_json::from_str(&json).unwrap();
        }
    }

    #[test]
    fn serde_peripheral_config_variants() {
        let configs = alloc::vec![
            PeripheralConfig::Adc {
                channel: 0,
                resolution_bits: 12,
                sample_time: 3
            },
            PeripheralConfig::Pwm {
                frequency_hz: 20_000,
                dead_time_ns: Some(100)
            },
            PeripheralConfig::Uart {
                baud: 115200,
                word_bits: 8,
                parity: Parity::None,
                stop_bits: 1
            },
            PeripheralConfig::Spi {
                frequency_hz: 1_000_000,
                mode: 0,
                bit_order: BitOrder::MsbFirst
            },
            PeripheralConfig::I2c {
                speed: I2cSpeed::Fast,
                address: 0x3C
            },
            PeripheralConfig::Gpio { pull: GpioPull::Up },
            PeripheralConfig::Encoder {
                counts_per_rev: 4096
            },
            PeripheralConfig::Can {
                bitrate: 500_000,
                fd_bitrate: Some(2_000_000)
            },
            PeripheralConfig::Timer {
                frequency_hz: 10_000
            },
            PeripheralConfig::None,
        ];
        for c in configs {
            let json = serde_json::to_string(&c).unwrap();
            let _: PeripheralConfig = serde_json::from_str(&json).unwrap();
        }
    }

    #[test]
    fn validate_bad_link_node_ref() {
        let mut m = example_manifest();
        m.topology.links[0].from_node = s("ghost");
        let errs = validate_manifest(&m).unwrap_err();
        assert!(errs.iter().any(|e| e.contains("unknown source node")));
    }
}
